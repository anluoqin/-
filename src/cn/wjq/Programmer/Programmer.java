package cn.wjq.Programmer;

public class Programmer {
	private String name;
	private String age;
	private String gao;
	private String xueli;
	private String jngyan;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGao() {
		return gao;
	}
	public void setGao(String gao) {
		this.gao = gao;
	}
	public String getXueli() {
		return xueli;
	}
	public void setXueli(String xueli) {
		this.xueli = xueli;
	}
	public String getJngyan() {
		return jngyan;
	}
	public void setJngyan(String jngyan) {
		this.jngyan = jngyan;
	}
	
	
}
